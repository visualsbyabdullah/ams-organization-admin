type InvoiceSeed = Omit<
  Invoice,
  | "subtotal"
  | "taxAmount"
  | "totalAmount"
  | "balanceAmount"
>;

function createInvoice(
  seed: InvoiceSeed,
): Invoice {
  const totals = calculateInvoiceTotals(
    seed.lineItems,
    seed.taxRate,
    seed.discountAmount,
  );

  return {
    ...seed,
    ...totals,
    balanceAmount: Math.max(
      totals.totalAmount - seed.paidAmount,
      0,
    ),
  };
}

function createRecurringInvoice(
  seed: Omit<
    RecurringInvoice,
    | "subtotal"
    | "taxAmount"
    | "totalAmount"
  >,
): RecurringInvoice {
  const totals = calculateInvoiceTotals(
    seed.lineItems,
    seed.taxRate,
    seed.discountAmount,
  );

  return {
    ...seed,
    ...totals,
  };
}

