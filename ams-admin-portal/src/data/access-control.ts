const ALL_ACTIONS = PERMISSION_ACTIONS.map((action) => action.id);

function createAllPermissions() {
  return Object.fromEntries(
    PERMISSION_MODULES.map((module) => [module.id, [...ALL_ACTIONS]]),
  ) as Record<string, PermissionAction[]>;
}

